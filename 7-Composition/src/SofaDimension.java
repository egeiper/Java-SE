public class SofaDimension {
    private int lenght;
    private int width;
    private int depth;

    public SofaDimension(int lenght, int width, int depth) {
        this.lenght = lenght;
        this.width = width;
        this.depth = depth;
    }

    public int getLenght() {
        return lenght;
    }

    public void setLenght(int lenght) {
        this.lenght = lenght;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getDepth() {
        return depth;
    }

    public void setDepth(int depth) {
        this.depth = depth;
    }
}
